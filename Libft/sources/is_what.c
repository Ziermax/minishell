int	ft_isspace(int c)
{
	return (c == ' ' || (c >= '\t' && c <= '\r'));
}

int	ft_isprint(int c)
{
	return (c >= ' ' && c <= '~');
}

int	ft_isacsii(int c)
{
	return (c >= 0 && c <= 127);
}
